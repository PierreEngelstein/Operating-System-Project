/* Common types redefinition */

#ifndef uint8
typedef unsigned char uint8;
#endif

#ifndef int8
typedef char int8;
#endif

#ifndef uint16
typedef unsigned short uint16;
#endif

#ifndef int16
typedef short int16;
#endif

#ifndef uint32
typedef unsigned int uint32;
#endif

#ifndef int32
typedef int int32;
#endif

#ifndef uint64
typedef unsigned long uint64;
#endif

#ifndef int64
typedef long int64;
#endif

#ifndef BOOL
typedef uint8 BOOL;
#endif

#ifndef VOID
typedef void VOID;
#endif

